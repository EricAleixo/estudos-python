#Desafio: O Desafio do Professor Klécio
#Contexto:
#O Professor Klécio adora desafiar seus alunos com perguntas de inglês. Só que é muito preguiçoso! Crie um programa onde faz perguntas e tenha as respostas também, e que siga as regras abaixo.

#Regras:
#O Professor Klécio vai fazer 3 perguntas.
#O jogador tem 2 tentativas para responder cada pergunta corretamente.
#Se acertar todas as perguntas, o jogador vence. Se errar duas vezes uma única pergunta, o jogo termina.